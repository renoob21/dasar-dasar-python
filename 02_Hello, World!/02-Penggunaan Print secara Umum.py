# Print dengan variabel

hewan1 = "kucing"
hewan2 = "naga"
bunga1 = "mawar"

print(hewan1)
print(hewan2)
print(bunga1)

# Print dengan beberapa argumen sekaligus

print(hewan1, hewan2, bunga1)

print(hewan1, "kuda", bunga1, 10)

# Argumen sep= dan end=

# sep
print(hewan1, "kuda", bunga1, 10, sep="-")

# end
print(hewan1, end=" ")
print(hewan2, end="/")
print(bunga1)
